num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = num1 + num2
produto = num1 * num2
media = (num1 + num2) / 2
maior = max(num1, num2)
menor = min(num1, num2)

print("Soma:", soma)
print("Produto:", produto)
print("Média:", media)
print("Maior número:", maior)
print("Menor número:", menor)